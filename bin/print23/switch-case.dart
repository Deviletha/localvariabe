void main(){

  var size = "L";

  switch(size){

    case 'XS':
      print('my shirt size is XS');
      break;
    case 'S' :
      print('my shirt size is S');
      break;
    case 'L' :
      print('my shirt size is L');
      break;
    case 'XL' :
      print('my shirt size is XL');
      break;
    case 'XXL' :
      print('my shirt size is XXL');
      break;
    default:
      print('none of these');
  }
}